#include <stdlib.h>
#include <stdio.h>
#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	max_len;

	i = 0;
	max_len = (size_t)ft_max(ft_strlen(s1), ft_strlen(s2)) + 1;
	if (n > max_len)
		n = max_len;
	while (i < n)
	{
		if (((unsigned char *)s1)[i] != ((unsigned char *)s2)[i])
			return (((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]);
		i++;
	}
	return (0);
}
